const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const rootDir = path.join(__dirname, '..');
const sourceFile = path.join(rootDir, 'pear-worker.js');
const bundleFile = path.join(rootDir, 'pear-worker.bundle');
const outputFile = path.join(rootDir, 'pear-worker.bundle.js');

if (!fs.existsSync(sourceFile)) {
  console.error('[build-pear-worker] Source not found:', sourceFile);
  process.exit(1);
}

console.log('[build-pear-worker] Bundling pear-worker.js with bare-pack...');

try {
  execSync(
    `npx bare-pack --linked --host ios --host android --format bundle --out "${bundleFile}" "${sourceFile}"`,
    { cwd: rootDir, stdio: 'inherit' }
  );
} catch (e) {
  console.error('[build-pear-worker] bare-pack failed.');
  process.exit(1);
}

const bundle = fs.readFileSync(bundleFile, 'utf-8');

// Escape the bundle string for embedding in JS
// The bundle contains newlines and quotes that need escaping
const escaped = bundle
  .replace(/\\/g, '\\\\')
  .replace(/"/g, '\\"')
  .replace(/\n/g, '\\n')
  .replace(/\r/g, '\\r');

const output = `// Auto-generated by scripts/build-pear-worker.js
// Source: pear-worker.js | Bundle: pear-worker.bundle
module.exports = "${escaped}";
`;

fs.writeFileSync(outputFile, output);
console.log(`[build-pear-worker] Wrote ${outputFile} (${bundle.length} bytes bundle)`);
