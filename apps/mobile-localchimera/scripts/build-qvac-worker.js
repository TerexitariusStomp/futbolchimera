const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const rootDir = path.join(__dirname, '..');
const sourceFile = path.join(rootDir, 'worker.js');
const bundleFile = path.join(rootDir, 'worker.bundle');
const outputFile = path.join(rootDir, 'worker.bundle.js');

if (!fs.existsSync(sourceFile)) {
  console.error('[build-qvac-worker] Source not found:', sourceFile);
  process.exit(1);
}

console.log('[build-qvac-worker] Bundling worker.js (QVAC inference) with bare-pack...');

try {
  execSync(
    `npx bare-pack --linked --host ios --host android --format bundle --out "${bundleFile}" "${sourceFile}"`,
    { cwd: rootDir, stdio: 'inherit' }
  );
} catch (e) {
  console.error('[build-qvac-worker] bare-pack failed. Ensure bare-pack is installed (npm i -D bare-pack).');
  process.exit(1);
}

const bundle = fs.readFileSync(bundleFile);
const base64 = bundle.toString('base64');

const output = `// Auto-generated by scripts/build-qvac-worker.js
// Source: worker.js | Bundle: worker.bundle
import b4a from 'b4a';
export default b4a.from('${base64}', 'base64');
`;

fs.writeFileSync(outputFile, output);
console.log(`[build-qvac-worker] Wrote ${outputFile} (${bundle.length} bytes bundle)`);
